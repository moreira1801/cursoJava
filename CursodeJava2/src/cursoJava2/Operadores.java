package cursoJava2;

public class Operadores {

	public static void main(String[] args) {
		int i = 10;
		System.out.println("Soma 10 + 5 = " + (i + 5));
		System.out.println("Subtração 10 - 5 = " + (i - 5));
		System.out.println("Multriplicação 10 * 5 = " + (i * 5));
		System.out.println("Divisão 10 / 5 = " + (i / 5));
		System.out.println("Resto da divisão 10 % 5 = " + (i % 5));
		i++;
		System.out.println("Adicionar 1 ao valor de i = " + i);
		i--;
		System.out.println("Subtrair  1 do valor de i = " + i);
	}

}
